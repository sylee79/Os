function getObjectClass(obj) {
    if (obj && obj.constructor && obj.constructor.toString) {
        var arr = obj.constructor.toString().match(
            /function\s*(\w+)/);

        if (arr && arr.length == 2) {
            return arr[1];
        }
    }

    return undefined;
}
jQuery(function ($) {
	var OSX = {
		container: null,
		originalAnchor: null,
		init: function () {
			$("input.osx, a.osx").click(function (e) {
				var modaltype = $(this).attr("modaltype");
				originalAnchor = $(this);
				e.preventDefault();

				$("#" + modaltype).modal({
					overlayId: 'osx-overlay',
					containerId: 'osx-container',
					closeHTML: null,
					minHeight: 80,
					opacity: 65, 
					position: ['0',],
					overlayClose: true,
					onOpen: OSX.open,
					onClose: OSX.close
				});
			});
		},
		open: function (d) {
			var self = this;
			var modaltype = $(originalAnchor).attr("modaltype");
			self.container = d.container[0];
			
			d.overlay.fadeIn('slow', function () {
				$("#" + modaltype, self.container).show();
				var title = $("#" + modaltype + " > .osx-modal-title", self.container);
				title.show();
				d.container.slideDown('slow', function () {
					setTimeout(function () {
						var h = $("#" + modaltype + " > .osx-modal-data", self.container).height()
							+ title.height()
							+ 20; // padding
						d.container.animate(
							{height: h}, 
							200,
							function () {
								$("div.close", self.container).show();
								$("#" + modaltype + " > .osx-modal-data", self.container).show();
							}
						);
					}, 300);
				});
			})
		},
		close: function (d) {
			var self = this; // this = SimpleModal object
			d.container.animate(
				{top:"-" + (d.container.height() + 20)},
				500,
				function () {
					self.close(); // or $.modal.close();
				}
			);
		}
	};

	OSX.init();

});